public class DiasDaSemana {
    public static void main(String[] args) {
        String diasDaSemana[] = {
            "Segunda",
            "Terça",
            "Quarta", 
            "Quinta",
            "Sexta",
            "Sábado",
            "Domingo"
        };

        // estrutura 'for-each'
        for (String dia : diasDaSemana) {
            System.out.println(dia);
        }
    }
}
